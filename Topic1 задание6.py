# Задание 6. Нахождение факториала числа

# 1. Запрашиваем число у пользователя
n = int(input("Введите целое число для расчета факториала: "))

# 2. Начальное значение факториала равно 1
factorial = 1

# 3. Используем цикл for для умножения чисел от 1 до n
# range(1, n + 1) позволяет включить само число n в расчет
for i in range(1, n + 1):
    factorial *= i

# 4. Выводим итоговый результат
print(f"Факториал числа {n} равен: {factorial}")
