# Задание 1. Целочисленное деление и остаток

# 1. Получаем данные от пользователя
# Функция int() преобразует введенный текст в целое число
A = int(input("Введите делимое (Число А): "))
B = int(input("Введите делитель (Число В): "))

# 2. Выполняем математические расчеты
# Оператор // находит целую часть от деления
result = A // B

# Оператор % находит остаток от деления
remainder = A % B

# 3. Выводим результат в нужном формате
print(f"Число А = {A}. Результат деления на В: {result}, остаток: {remainder}.")
