# Задание 5. Таблица умножения

# 1. Запрашиваем число у пользователя
# Функция int() преобразует ввод в целое число
number = int(input("Введите число для составления таблицы умножения: "))

print(f"Таблица умножения для числа {number}:")

# 2. Используем цикл for для вывода таблицы от 1 до 10
# range(1, 11) создает последовательность чисел: 1, 2, ..., 10
for i in range(1, 11):
    result = number * i
    # Выводим строку в формате: 5 x 1 = 5
    print(f"{number} x {i} = {result}")
