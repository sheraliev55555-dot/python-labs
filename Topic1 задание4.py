# Задание 4. Конвертер метров в километры

# 1. Запрашиваем расстояние у пользователя
# Используем float, чтобы можно было вводить дробные значения (например, 500.5)
meters = float(input("Введите расстояние в метрах: "))

# 2. Вычисляем количество километров
# В одном километре ровно 1000 метров
kilometers = meters / 1000

# 3. Выводим итоговый результат
print(f"Расстояние в метрах: {meters} м")
print(f"Расстояние в километрах: {kilometers} км")
